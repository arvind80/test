   <h3>ISO 14000</h3>
   <p>
    The ISO 14000 is a standard for environmental management systems
    that is applicable to any business, regardless of size, location or
    income. The aim of the standard is to reduce the environmental
    footprint of a business and to decrease the pollution and waste a
    business produces. The most recent version of ISO 14001 was released in
    2004 by the International Organization for Standardization (ISO) which
    has representation from committees all over the world. The ISO 14000
    environmental management standards exist to help organizations minimize
    how their operations negatively affect the environment. In structure it
    is similar to ISO 9000 quality management and both can be implemented
    side by side. In order for an organization to be awarded an ISO 14001
    certificate they must be externally audited by an audit body that has
    been accredited by an accreditation body. In the UK, this is UKAS.
    Certification auditors need to be accredited by the International
    Registrar of Certification Auditors. The certification body has to be
    accredited by the Registrar Accreditation Board in the USA, or the
    National Accreditation Board in Ireland.<br />
    <br />
    &nbsp;&nbsp;&nbsp; * ISO 14001 Environmental management systems&mdash;Requirements with guidance for use<br />
    &nbsp;&nbsp;&nbsp; * ISO 14004 Environmental management systems&mdash;General guidelines on principles, systems and support techniques<br />
    &nbsp;&nbsp;&nbsp; * ISO 14015 Environmental assessment of sites and organizations<br />
    &nbsp;&nbsp;&nbsp; * ISO 14020 series (14020 to 14025) Environmental labels and declarations<br />
    &nbsp;&nbsp;&nbsp; * ISO 14031 Environmental performance evaluation&mdash;Guidelines<br />
    &nbsp;&nbsp;&nbsp; * ISO 14040 series (14040 to 14049), Life Cycle Assessment, LCA,
    discusses pre-production planning and environment goal setting.<br />
    &nbsp;&nbsp;&nbsp; * ISO 14050 terms and definitions.<br />
    &nbsp;&nbsp;&nbsp; * ISO 14062 discusses making improvements to environmental impact goals.<br />
    &nbsp;&nbsp;&nbsp; * ISO 14063 Environmental communication&mdash;Guidelines and examples<br />
    &nbsp;&nbsp;&nbsp; * ISO 19011 which specifies one audit protocol for both 14000 and
    9000 series standards together. This replaces ISO 14011
    meta-evaluation&mdash;how to tell if your intended regulatory tools worked.
    19011 is now the only recommended way to determine this.</p>

      independent consultants, they examine plants, motor vehicles, energy
      installations, devices and products (e.g consumer goods) which require
      monitoring. The many subsidiaries of the T&Uuml;Vs can also act as project
      developers for energy and traffic concepts, as problem solvers in
      environmental protection, and as certification bodies. Many of the T&Uuml;V
      organizations also provide certification for various international
      standards, such as ISO9001:2008 (quality management system) and
      ISO/TS16949 (automotive quality management system).

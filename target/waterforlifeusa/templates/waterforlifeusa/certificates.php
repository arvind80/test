    <h3>T&Uuml;V</h3>
    <p>
      T&Uuml;Vs (German pronunciation: [?t?f]) (short for Technischer
      &Uuml;berwachungs-Verein, Technical Inspection Association in English) are
      German organizations that work to validate the safety of products of

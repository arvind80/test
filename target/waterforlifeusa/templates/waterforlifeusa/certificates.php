    <h3>CSA</h3>
<p>
International (Canadian Standards Association), a member of the CSA
Group, is a provider of product testing and certification services for
electrical, mechanical, plumbing, gas and a variety of other products.
Recognized in the U.S., Canada and around the world, CSA&rsquo;s marks appear
on billions of products worldwide.<br />
CSA International certification marks indicate that a product, process
or service has been tested to a Canadian or U.S. standard and it meets
the requirements of an applicable CSA standard or another recognized
document used as a basis for certification.<br />
For consumers, CSA International certification marks are intended to
provide increased assurance of quality and safety. For manufacturers,
international recognition of the mark may help to ease their entry into
North American markets. CSA International certification marks are
accepted by many North American regulators and by a large number of
North American retailers. Billions of products bearing CSA
International certification marks are found on the shelves of
well-known retail chains and sold by major product distributors.<br />
CSA International tests products for the North American and
International markets against applicable standards including those of
the Canadian Standards Association (CSA), Underwriters Laboratories
(UL), NSF National Sanitation Foundation (NSF), American National
Standards Institute (ANSI) and other agencies.</p>


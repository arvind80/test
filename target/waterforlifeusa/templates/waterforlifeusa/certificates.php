    <h3>GMP</h3>
    <p>GMP is a system to ensure products are consistently produced and controlled according to quality standards. It is designed to minimize the risks involved in any pharmaceutical production that cannot be eliminated through testing the final product. A directive circular issued by the Ministry of Health in Jul 95 marked the official launch of GMP certification in China. The China Certification Committee for Drugs (CCCD) was established in the same year. A subsidiary organization was also set up to manage the certification program.</p>
    <p>Currently nine government agencies are the key agencies responsible for regulation. They are the State Food and Pharmaceutical Administration (SFDA), the State Development and Reform Committee, the Commerce Ministry, the State Traditional Chinese Medicine Administration, the Ministry of Labor and Social Security, the Ministry of Health, the State Population and Family Planning Committee, the Ministry of Science and Technology, and the State Quality and Technology Supervision Administration.</p>
    <p>In addition, more than 10 industrial associations also regulate the industry.</p>

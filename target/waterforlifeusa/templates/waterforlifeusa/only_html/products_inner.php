                    <p class="pborder">Cutting edge slotted plate technology!  The platinum dipped titanium plates incorporate KYK's slotted plate technology, which has the best of all worlds -- extremely long life-span, optimal electrolysis and optimal ionization -- with none of the worries that go along with mesh plates, nor the lesser ionization of non-slotted plates.    </p>

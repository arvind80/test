                    <p class="pborder">76 programmable pH and ORP levels!  The KYK Genesis allows you to program nearly any pH and ORP combination, so as to customize your pH and ORP to the level that's right for you.  This feature is essential due to the wide range of tap water quality in North America.  Non-programmable ionizers cannot adjust to local tap water conditions, so these ionizers are at the mercy of your tap water and a handful of pre-set pH levels.  The actual pH levels of non-programmable ionizers can vary greatly from the pH levels posted on the control panel of the unit.   Simply put, when used in two different cities or states, a non-programmable ionizer will typically get two completely different pH level outputs when kept at the same pH setting. </p>
